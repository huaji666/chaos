package chaos.core.utils.constant;

public interface Constant {

    interface DateFormat {
        /**
         * 2009-03-20 22:24:30
         */
        String yyyy年MM月dd日HH_mm分 = "yyyy年MM月dd日 E a h:mm";

        /**
         * 2009-03-20 22:24:30
         */
        String MM月dd日HH_mm分 = "MM月dd日 E a h:mm";

        /**
         * 2009-03-20 22:24:30
         */
        String yyyy_MM_dd_HH_mm_ss = "yyyy-MM-dd HH:mm:ss";
        /**
         * 2009-03-20
         */
        String yyyy_MM_dd = "yyyy-MM-dd";
    }

    interface coding {
        String UTF8 = "utf-8";
        String GBK = "bgk";
    }


}